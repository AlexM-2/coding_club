#creates a list of odd numbers using the third parameter
print("Prints odd numbers from 1-20 using the third parameter in the range() function:")
oddNumbers = list(range(1,20,2))
print(oddNumbers)
print()

#prints all the odd numbers using a for loop
print("Prints odd numbers from 1-20 using a for loop:")
for num in oddNumbers:
	print(num)